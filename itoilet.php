<h2>Kitale, Kenya</h2>
<p>In Kitale, Kenya, many schools faced closure - not because of governmental reforms, lack of funds or a shortage of resources - but because of their sanitation. Without clean toilets, children couldn't go to school. But just £50 is enough to help keep a school open - this is all is costs to build a Bio-latrine.</p>
<p>Because they are a “dry” toilet, they neither require water to work (which is already scarce) nor is there any danger of waste leaching into the groundwater and polluting water supplies, causing disease.</p>
<p>The bio-latrine system also brings with it a surprising and marvellous range of added benefits, including helping to both heat and light the school.</p>
<p>Practical Action has already helped four schools in Kitale to install improved latrine systems, helping to keep 3,000 children in school. The benefits to the children and their schools are immense.</p>
<p>Shimo La Tewa school in Kitale now has new bio latrines to help improve sanitation and health at the school. Methane gas generated from the latrines is used for lighting in the evenings and cooking. After the gas has been removed the bio waste is used in the school garden as a fertiliser.</p>
<h2>Sicuani, Peru</h2>
<p>Practical Action has introduced 300 new eco-toilets to the village of Sicuani in Peru. The eco-toilets are a dry loo (essential in an area where water is extremely scarce). They have been developed with the local communities so that the design is one which can be built readily with local materials and is easy for local people to build.</p>
<h2>Epworth Bellapaise, Zimbabwe</h2>
<p>Epworth Bellapaise was the epicentre for cholera outbreaks prior to 2007, largely due to the lack of suitable toilets in the area and the prevalence of open defecation. Reports from the project’s ‘Peer Educators’, (health and hygiene education promoters) indicate a significant reduction in the incidence of diarrhoea.</p>
<p>Between January 2008 and January 2009 there was no cholera outbreak in the target area (which is hugely significant given the social, economic and infrastructural breakdown in Zimbabwe during the same period).</p>
<p>This can, in large part, be attributed to the construction of ecosan latrines and the intense health and hygiene education carried out between September 2007 and January 2009.</p>
<p>The ecosan toilets have combated the communities’ tendency to open defecation, which has greatly reduced the occurrence of cholera outbreaks in the area. Previously, the absence of toilets meant that families practiced open defecation and come the rainy season, faecal matter would flow into the shallow wells which the community used to collect their drinking water. It is during this process that the water borne diseases would be transmitted and outbreaks would occur. So the construction of the ecosan toilets has cut off this disease transmission route and prevented the outbreak of cholera in the project area.</p>
