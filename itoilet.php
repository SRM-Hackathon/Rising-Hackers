      <h2>Better sanitation to reduce disease</h2>
      <p style="font-size:10px";>In areas where water is scarce and plumbing doesn't exist, communities are forced to share communal pit latrines, which are little more than holes in the ground framed by planks of wood. The even more unhygienic alternative is called a flying toilet, which simply consists of a plastic bag; these litter the slums. Both toilets pose huge risk of disease which in many cases can prove fatal.</p>
    <p style="font-size:10px";>Practical Action use a variety of different technologies, as appropriate to each community, to help improve sanitation and health.</p>
