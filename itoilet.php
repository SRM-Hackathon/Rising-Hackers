    <h2>Bio-latrines</h2>
    <p>Bio-latrines use a dry toilet technology which reduces the demand for water. The bio-latrine includes a natural exhausting process so that the digester system never fills up to overflow. The waste collected in the digester is processed using anaerobic digestion to make organic manure (suitable for use as fertiliser). As the waste biodegrades, the digester captures methane gas which is used for lighting and cooking.</p>
    <img src="https://d1x0je2yh2wyb8.cloudfront.net/b/i/4fa25770-c088-46c1-90f1-1a490ae4f5bb.WyI2MDB4NjAwIiwic2NhbGUiXQ.png" alt="">
    <h2>Pour-flush toilets</h2>
    <p>Pour-flush toilets are a simple technology that keeps sewage contained and off the streets. Any water left from washing or cooking, can be used to flush the toilet, and a simple water valve keeps in smells and stops flies from getting in.</p>
    <p>The septic tanks are lined, to prevent the sewage from contaminating water supplies, and are emptied safely by trained pit emptiers, who can earn a living.</p>
<h2>Ecological sanitation (Ecosan)</h2>
<p>Ecological sanitation (Ecosan) is an alternative holistic approach for healthy and economically sustainable sanitation. This approach is based on the consistent implementation of the “closing the loop approach” (Nutrient Cycling), where urine and faeces are regarded as resources rather than waste. If collected separately and sanitized they can be used as organic fertilizer or as soil conditioner. It is an approach that saves water, protects water quality, prevents pollution and returns valuable nutrients to the soil.</p>
<h2>Ventilated improved latrines (VIP)</h2>
<p>Ventilated improved latrines (VIP) are equipped with ventilation pipes to get rid of flies and smells, and a concrete platform that’s easy to keep clean. These improved loos have proven so effective that local people have taken the initiative and built many more.</p>
<h2>SULAV twin-pit latrines</h2>
<p>SULAV twin-pit latrines are an effective,easy-to-maintain toilet where the waste is sealed to remove pathogens before being composted and used as manure. When one pit is full, it’s closed and the other is used. After a year, the first pit can be safely emptied and the contents used as manure.</p>
