<p>Several levels of washing can be achieved manually or mechanically: simply cleaning with a soft brush, washing in water with a brush or with detergent, washing in a caustic soda solution in case of resistant contaminants (oil, grease). When impurities are difficult to remove, the contaminated part of the material can be cut off.</p>
<p>Cleaning can occur before and/or after the sorting. It is followed by manual or mechanical drying. It can consist of simply placing recovered waste to drain in the sun on a plastic sheet (to avoid dust), centrifuging shredded material or using some hot air blowing device. Plastics films can be hung on wires to save space. When left to drain, plastics material usually has to be stirred occasionally. In India, the waste plastics are washed in a large concrete basin with water pumped by a small electric engine before sorting. The washed plastics are then dried in a rotating mesh drum and spread out on the ground to dry in the sun.</p>
<p>Converting shredded or agglomerated plastics into pellets is recommended before further processing. A pelletising machine consists of an extruder, a cooling bath and a cutting machine. During the melting caused by the extruder, the plastic can be homogenised, degassed and coloured; it is forced by a screw down the barrel through a filter screen aiming at removing solid particles and then through the extrusion die head; spaghettis are formed which are then cooled and cut into pellets. Pellets can then be used as the input materials for various moulding processes</p>
