<h2>Making money out of waste</h2>
<p style="text-size:10px";>Waste plastics are often contaminated with sticky liquids and organic matter, making the sorting a dirty job and the further processing steps more difficult, and reducing the product quality.</p>
<p>Plastics washing machines are specially designed to wash plastic solid waste materials before they are sold on for commercial recycling where plastic pellets can be turned into new produces such as fence posts.</p>
