      <h2>Reducing plastic waste in Nepal</h2><br>
      <p>Working with partner organisations, Practical Action has provided support with training activities and field visits to help households understand solid waste management systems in Nepal.</p>
      <p>Before 2003 residents were careless about the problems associated with excessive use of plastics, and it was a common sight to see garbage lying in public areas. Since then, significant environmental improvements have been achieved by local women’s groups who have taken action to reduce the amount of waste plastic in eight areas of Bharatupur district, Nepal.</p>
      <p>Residents have been encouraged to segregate plastic bags from other waste and push them onto a suiro, which is a metal hook with a long stem that can hold large numbers of bags so they are not scattered to the winds. Using the suiro has helped to reduce the quantities of mixed waste and the careless dumping of waste. This is not only reducing the amounts of plastic waste in the urban area but also making people aware of the environmental problems caused by plastics.</p>
      <p>The women’s groups collect around two tonnes of plastics every month, more than a tonne of plastics can be sold to generate an income. The plastic is collected using two rickshaws that can carry 300-500 kg of recovered waste that is transported to the dumping site designated by Bharatpur municipality.</p>
      <p>With Practical Action’s support the programme has demonstrated that an income can be generated from solid waste and other areas are now adopting the scheme.</p>
