      <img src="https://d1x0je2yh2wyb8.cloudfront.net/1/5/4d835ccc-6aec-4d6f-8ab0-57b61661b3dc.WyI2MDB4NjAwIiwic2NhbGUiXQ.jpg" alt="">
      <h2>First prize for Tulasa</h2><br>
      <p>Tulasa Gyawali from Syauli Baazar, 10 Bharatpur, Nepal, won first prize in a national innovation fair and was asked to share her story in an international symposium for her continuous effort on utilising waste in urban agriculture.</p>
      <p>“I was honoured when I received the first prize. In my house, I have a compost bin and pits and I practice vermin composting too. I do not throw decomposable waste; instead I convert them to resources and use them to grow organic vegetables, she smiles.”</p>
      <p>The Integrated Sustainable Waste Management (ISWM) project in collaboration with Bharatpur Municipality had earlier distributed compost bins in Syauli Baazar. Residents of the community do not throw wastes in the street corner as before. People are making compost from organic waste. They separate plastic in their homes and they sell them to the plastic collectors.</p>
      <p>According to Tulasa, “this is one of the positive changes from the project. Waste management training to the community has changed people’s attitude toward disposing waste. Now we think waste as a resource.”</p>
      <p>Tulasa and other community members were taken to India for a waste management exposure visit. “The visit was educating and motivating for me and to my neighbours. We observed the good practice in Forum of Recycle and Environment (FORCE), Mumbai and now we have replicated that practice in our community.”</p>
      <p>In Tulasa’s community, 10 households have built a masonry compost pit in order to accommodate more organic waste and to sell the compost to the local nursery and farmers. This is an example of how awareness can make a difference.</p>
