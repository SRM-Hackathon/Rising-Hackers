      <div class="t1";>
      <p>Composting reduces the volume of generated wastes that would have to be transported and disposed of. It offers several benefits such as enhanced soil fertility and soil health-thereby increased agricultural productivity, improved soil biodiversity, reduced ecological risks and a better environment.</p>
      <p>Composting is the biodegradation of organic matter under aerobic conditions where materials are transformed into compost. Dead leaves, kitchen wastes and vegetables are considered as organic wastes. Compost is odorless and pathogen free brown mixture that is specially used as soil conditioner, if it accurately decomposed.</p>
      <p>Home composting is now being encouraged as a means of reducing the organic waste being discarded and sent to the landfills. These organic substances are bulky to handle and contribute to numerous liquid and gaseous emissions that deteriorate dumpsite environments. A good home composting programmes can significantly reduce the quantity of organic waste adding to the mainstream and subsequent emissions upon final disposal. Valuable products (compost) are produced while reducing the costs incurred for collection, transportation and final disposal at dumpsite.</p>
